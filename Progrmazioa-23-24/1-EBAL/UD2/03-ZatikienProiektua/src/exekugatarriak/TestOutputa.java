package exekugatarriak;

public class TestOutputa {
    public static void main(String[] args) {
        System.out.println("16/8 = " + 16 / 8);
        System.out.println("8/16 = " + 8 / 16);
        System.out.println("8/16 = " + 8 / 16.0);
        System.out.println("8/16 = " + (double) 8 / 16);
        System.out.println("16/0.0 = " + 16 / 0.0);
        System.out.println("16/0 = " + 16 / 0);
    }
}