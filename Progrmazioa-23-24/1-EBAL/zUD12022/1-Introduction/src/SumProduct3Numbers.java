public class SumProduct3Numbers {
    public static void main(String[] args) {
        int number1 = 98; 
        int number2 = 5;
        int number3 = 77;  
        int sum, product;  

        sum = number1 + number2 + number3;
        product = number1 * number2 * number3;
        
        System.out.println("The sum is: " + sum); 
        System.out.println("The product is: " + product); 

     }    
}
